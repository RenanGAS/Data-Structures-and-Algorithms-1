#include <stdio.h>

void imprimirVetor(int *v, int tam)
{
    int i;
    printf("[");
    for (i = 0; i < tam; i++)
    {
        printf("%d", v[i]);
        if (i < tam - 1)
        {
            printf(",");
        }
    }
    printf("]");
    printf("\n");
}

